			<p>Diet sehat pertama yang perlu Anda lakukan adalah dengan olahraga secara rutin. Olahraga yang dimaksud bukanlah naik tangga kantor atau memilih berjalan kaki menuju kantor, ya. Olahraga untuk menurunkan berat badan benar-benar perlu terprogram dengan baik. Mengutip dari Diet Doctor (dietdoctor.com), Anda perlu olahraga minimal 30 hingga 60 menit per hari. Namun, pastikan untuk tidak mengonsumsi sport drink, ya. Jenis minuman itu dapat membuat proses menurunkan berat badan menjadi kurang efektif.</p>

			<h2>2. Hindari Stres</h2>
			<p>Cara diet sehat selanjutnya yang sebaiknya Anda coba adalah dengan menghindari stres berlebih dan kurang tidur. Dua hal itu dapat meningkatkan hormon stres dalam tubuh sehingga rasa lapar pun meningkat. Artinya, keinginan untuk mengonsumsi makanan tidak dapat Anda hindari. Cobalah untuk mengelola stres dengan baik. Misalnya, lakukan hobi atau kegiatan yang bisa membuat tubuh dan pikiran rileks, menulis jurnal, dan lain sebagainya. Mengatur waktu tidur menjadi lebih awal pun bisa Anda lakukan.</p>

			<h2>3. Kurangi Konsumsi Produk Olahan Susu</h2>
			<p>Produk olahan susu atau dairy product memang terasa lezat. Sayangnya, jenis makanan itu tidak cocok dimasukkan dalam menu diet sehat. Jenis makanan itu mengandung banyak laktosa sehingga proses penurunan berat badan menjadi lebih lambat.</p>

			<h2>4. Jangan Terlalu Banyak Makan Kacang</h2>
			<p>Kacang adalah salah satu jenis makanan yang mengandung banyak karbohidrat, apalagi kacang mete. Jenis kacang itu mengandung karbohidrat sebanyak 20% sehingga tidak cocok dikonsumsi ketika diet. Jadi, cobalah untuk lebih berhati-hati ketika makan kacang supaya berat badan bisa turun secara efektif.</p>

			<h2>5. Jangan Minum Bir</h2>
			<p>Bir adalah salah satu jenis minuman beralkohol yang sebaiknya tidak Anda konsumsi secara berlebih. Sama seperti poin sebelumnya, konsumsi bir dapat menurunkan proses pembakaran lemak sehingga berat badan sulit turun. Jika Anda benar-benar ingin mengonsumsi minuman beralkohol, cobalah untuk mengonsumsi jenis minuman yang lain. Mulai dari wine, dry champagne, atau hard liquor. Meski demikian, Anda tetap perlu mengatur porsi minuman yang Anda konsumsi.</p>
