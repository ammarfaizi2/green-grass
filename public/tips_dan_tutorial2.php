		<h1>Tutorial Membuat Sayur Asem</h1>
		<img src="assets/img/sayur_asem.jpg" alt="Sayur Asem"/>
		<div style="text-align:left;">
			<p>Jika sayur asem yang Anda masak biasanya tidak pakai sayuran kangkung, kali ini Anda patut mencobanya. Kangkung bukan hanya bisa dimasak tumis atau dibuat cah, tetapi juga dapat menjadi bahan utama dalam sayur asem. Kangkung memiliki banyak manfaat untuk kesehatan. Di antaranya menjaga kesehatan mata hingga mencegah dehidrasi.</p>
			<h2>Bahan:</h2>
			<ul>
				<li>3 ikat kangkung, bersihkan dan potong</li>
				<li>100 gram kacang merah segar</li>
				<li>4 buah tomat, potong jadi empat bagian</li>
				<li>700 cc air</li>
			</ul>
			<h2>Bumbu:</h2>
			<ul>
				<li>2 siung bawang putih</li>
				<li>7 siung bawang merah</li>
				<li>3 buah cabai merah besar</li>
				<li>2 butir kemiri</li>
				<li>1/2 sdt terasi matang</li>
				<li>Sepotong lengkuas, memarkan</li>
				<li>3 lembar daun salam</li>
				<li>Air asam secukupnya sesuai selera</li>
				<li>Garam secukupnya</li>
				<li>Gula secukupnya</li>
			</ul>
			<h2>Cara Pembuatan:</h2>
			<ol>
				<li>Rebus kacang merah sampai lunak.</li>
				<li>Haluskan bawang putih, bawang merah, cabai merah, kemiri, dan terasi matang.</li>
				<li>Didihkan air di panci dan masukkan semua bumbu yang telah dihaluskan.</li>
				<li>Tambahkan daun salam, lengkuas, air asam, garam, dan gula.</li>
				<li>Masukkan kangkung, tomat, dan kacang merah secara bersamaan. Masak hingga matang.</li>
			</ol>
			<p>Demikian cara membuat sayur asem sapi istimewa ala rumahan. Tak kalah enak dengan bikinan resto, kok.</p>
