		<h1>Tips Hidup Sehat</h1>
		<img src="assets/img/sayur.jpg" alt="Sayur"/>
		<h2>Memulai cara hidup sehat</h2>
		<div style="text-align:left;">
			<p>Melakukan pola hidup sehat pada dasarnya tidak ribet, asalkan Anda konsisten dan rutin melakukannya. Beberapa langkah sederhana yang dapat Anda terapkan sebagai cara hidup sehat, di antaranya:</p>

			<h2>1. Sempatkan berolahraga</h2>
			<p>Orang dewasa disarankan untuk berolahraga minimal 2Â½ jam setiap minggunya. Sempatkan untuk berolahraga karena melakukan peregangan bagus untuk tubuh dan pikiran Anda. Lakukan olahraga yang Anda sukai, misalnya berlari, bersepeda, berenang, senam, yoga, dan lainnya.Anda juga dapat melakukan latihan di pusat kebugaran untuk membantu membakar kalori dan membentuk massa otot. Banyak ahli juga merekomendasikan untuk berjalan setidaknya 10.000 langkah per hari dalam menjaga kesehatan tulang.</p>

			<h2>2. Tidur yang cukup</h2>
			<p>Sebagian besar orang dewasa membutuhkan sekitar 7-9 jam untuk tidur di malam hari, meski setiap orang mungkin berbeda kebutuhannya. Tidur yang cukup dapat membuat suasana hati menjadi lebih baik, mempertajam memori dan fokus, serta membantu mempelajari hal-hal baru.Dalam jangka panjang, cara ini dapat menurunkan risiko penyakit jantung dan menjaga berat badan Anda. Akan lebih baik jika Anda tidur dan bangun di waktu yang sama setiap hari.</p>

			<h2>3. Minum air putih yang cukup</h2>
			<p>Sebagian besar orang dewasa membutuhkan sekitar 8 gelas atau 2 liter air putih setiap hari agar tetap terhidrasi dengan baik. Anda juga dapat menambahkan irisan lemon, jeruk atau timun untuk membuat air menjadi lebih segar.Sebaiknya, hindari minuman ringan bergula dan minuman berenergi karena termasuk sumber utama kalori yang tak bernutrisi, serta dapat menyebabkan obesitas dan diabetes tipe-2. Selain itu, hindari pula alkohol karena dapat memberi efek negatif pada tubuh.</p>

			<h2>4. Memilih makanan yang baik</h2>
			<p>Pola makan yang sehat dan seimbang dapat membantu tubuh menyediakan energi dan menurunkan risiko seseorang terkena penyakit kronis, seperti jantung, hipertensi, diabetes, dan kanker. Ini juga dapat membantu Anda mempertahankan berat badan normal.Pastikan dalam piring Anda terdapat cukup protein yang berasal dari hewani dan nabati, karbohidrat, serat, serta lemak yang baik. Perbanyak konsumsi sayur, buah, kacang-kacangan, dan biji-bijian. Hindari berbagai makanan olahan dan makanan yang mengandung banyak gula.</p>

			<h2>5. Menjaga berat badan ideal</h2>
			<p>Obesitas dikaitkan dengan usia yang lebih pendek dan peningkatan risiko berbagai penyakit kronis. Oleh sebab itu, sangat penting bagi Anda untuk menjaga berat badan dalam kisaran yang normal.Mengonsumsi makanan sehat dan rutin berolahraga dapat membantu Anda mempertahankan atau mencapai berat badan ideal.</p>

			<h2>6. Hindari rokok</h2>
			<p>Menurut ahli, merokok menjadi salah satu pemicu terbesar timbulnya berbagai penyakit yang dapat menyebabkan kematian. Jika Anda ingin mewujudkan hidup sehat, tentu saja Anda harus berhenti merokok.Meski berhenti merokok bukan perkara mudah, sebaiknya pikirkan berbagai konsekuensi buruk akibat merokok yang dapat terjadi pada Anda dan keluarga. Rokok dapat merusak paru-paru dan jantung, disfungsi ereksi, bahkan mampu mempercepat kerutan pada kulit.</p>

			<h2>7. Memelihara kesehatan mental</h2>
			<p>Kesehatan mental sama pentingnya dengan kesehatan fisik. Kesehatan mental yang buruk dapat mempngaruhi energi dan fokus Anda, bahkan meningkatkan peluang terkena penyakit jantung.Jika Anda merasa stres, cemas, depresi, atau masalah lainnya, Anda dapat membicarakannya pada pasangan, keluarga atau teman untuk meringankan perasaan Anda. Namun bila itu tidak menolong, Anda dapat meminta bantuan pada psikolog.</p>

			<h2>8. Sempatkan berjalan-jalan</h2>
			<p>Beberapa menit berada di bawah sinar matahari pagi, dapat meningkatkan kadar vitamin D yang baik untuk tulang, jantung, dan suasana hati Anda.Berada di luar ruangan juga cenderung membuat tubuh Anda lebih aktif bergerak. Akan lebih baik jika Anda sempatkan untuk berjalan-jalan di alam terbuka yang dapat membuat perasaan lebih tenang dan bahagia.</p>

			<h2>9. Rutin cek kesehatan</h2>
			<p>Lakukan pemeriksaan kesehatan secara rutin untuk memudahkan Anda mendeteksi suatu penyakit atau masalah kesehatan lebih dini. Pemeriksaan kesehatan yang paling umum dapat Anda mulai dari pengukuran berat dan tinggi badan, tekanan darah, hingga cek dan deteksi dini kanker.</p>

			<h2>10. Menjaga kebersihan lingkungan</h2>
			<p>Menjaga kebersihan lingkungan dalam skala kecil, dapat dilakukan dengan pengelolaan sampah yang benar. Langkah lain yang dapat dilakukan adalah menjaga kebersihan untuk mengurangi resiko gangguan kesehatan, seperti mencegah perkembangan sumber penyakit yang ada di lingkungan sekitar.</p>
