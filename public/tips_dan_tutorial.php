		<h1>Tutorial Memasak Sup Iga</h1>
		<img src="assets/img/sup_iga.jpg" alt="Sup Iga"/>
		<div style="text-align:left;">
			<p>Selain sop ayam, sop iga sapi juga sangat digemari para pecinta kuliner. Banyak kedai maupun restoran yang khusus menyediakan masakan ini. Tetapi sebenarnya kita juga bisa menyajikan sendiri di rumah jika mengetahui cara membuat sop iga yang enak. Selanjutnya kita akan belajar cara membuat sop iga sapi istimewa. Berikut ini resep lengkapnya.</p>
			<h2>Bahan:</h2>
			<ul>
				<li>3 buah tomat (iris)</li>
				<li>2 buah kentang (potong dadu)</li>
				<li>2 liter air</li>
				<li>2 buah wortel (potong bulat)</li>
				<li>1 batang seledri (iris)</li>
				<li>1 batang daun bawang (iris)</li>
				<li>1/2 bawang bombay (potong kotak)</li>
				<li>1 kilogram iga sapi</li>
			</ul>
			<h2>Bumbu:</h2>
			<ul>
				<li>3 siung bawang putih (cincang)</li>
				<li>3 butir cengkeh</li>
				<li>2 cm kayu manis</li>
				<li>1 sendok makan gula pasir</li>
				<li>1 sendok teh kaldu sapi bubuk (opsional)</li>
				<li>1/2 sdt lada hitam</li>
				<li>1/2 butir pala (parut)</li>
				<li>Garam secukupnya</li>
			</ul>
			<h2>Pelengkap:</h2>
			<ul>
				<li>Bawang goreng secukupnya</li>
				<li>Jeruk nipis secukupnya</li>
			</ul>
			<h2>Cara Pembuatan:</h2>
			<ol>
				<li>Panaskan air, kemudian masukkan iga sapi. Masukkan bumbu-bumbu dan bawang bombay yang sudah dipotong dadu. Masak sampai iga sapi empuk. Jika airnya menyusut terlalu banyak boleh ditambahkan air lagi.</li>
				<li>Setelah iga cukup empuk, masukkan wortel dan kentang. Masak sampai matang dan koreksi rasa. Tambahkan garam jika dirasa masih kurang gurih.</li>
				<li>Sebelum sop diangkat dari kompor, masukkan tomat, daun bawang, dan seledri.</li>
				<li>Sajikan sop iga sapi dengan bawang goreng dan kucuran jeruk nipis.</li>
			</ol>
			<p>Demikian cara membuat sop iga sapi istimewa ala rumahan. Tak kalah enak dengan bikinan resto, kok.</p>
